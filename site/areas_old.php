<p class="tit2">Contratual</p><br />
 <p class="texto">A assessoria na negocia&ccedil;&atilde;o e celebra&ccedil;&atilde;o de contratos, nacionais e internacionais, envolve tanto a consultoria preventiva quanto o contencioso judicial em quest&otilde;es de naturezas civil e comercial. <br /><br />
Entre as atividades realizadas destacam-se a elabora&ccedil;&atilde;o e/ou revis&atilde;o de diferentes tipos de contratos (compra e venda mercantil, aliena&ccedil;&atilde;o fiduci&aacute;ria em garantia, arrendamento mercantil, transporte de bens e de pessoas, loca&ccedil;&atilde;o, seguro, franquia, venda em consigna&ccedil;&atilde;o, doa&ccedil;&atilde;o, incorpora&ccedil;&atilde;o imobili&aacute;ria, entre outros), orienta&ccedil;&atilde;o durante todo o processo de negocia&ccedil;&atilde;o at&eacute; a assinatura dos contratos, incluindo, se necess&aacute;rio, a representa&ccedil;&atilde;o e/ou acompanhamento do cliente em reuni&otilde;es, com a finalidade de garantir a seguran&ccedil;a jur&iacute;dica aos neg&oacute;cios e atividades desenvolvidas pelos clientes.<br /><br />
A consultoria nessa &aacute;rea inclui tamb&eacute;m a orienta&ccedil;&atilde;o para solu&ccedil;&atilde;o de problemas relacionados &agrave; execu&ccedil;&atilde;o das obriga&ccedil;&otilde;es contratuais, bem como quest&otilde;es relacionadas &agrave; rescis&atilde;o dos contratos.</p>
