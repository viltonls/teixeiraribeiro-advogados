Presta orienta&ccedil;&atilde;o em quest&otilde;es espec&iacute;ficas levadas ao conhecimento dos &oacute;rg&atilde;os encarregados da prote&ccedil;&atilde;o ao consumidor (PROCON), bem como representa&ccedil;&atilde;o e orienta&ccedil;&atilde;o em inqu&eacute;ritos civis, processados perante o Minist&eacute;rio P&uacute;blico, assim como defesa quando resultantes em a&ccedil;&atilde;o civil p&uacute;blica. <br /><br />
