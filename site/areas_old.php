<p class="tit2"> <a name="tributario" id="tributario"></a>Tributário</p><br />
  <p class="texto">Teixeira Ribeiro Advogados atua em planejamento legal tribut&aacute;rio, envolvendo tamb&eacute;m as opera&ccedil;&otilde;es societ&aacute;rias e comerciais necess&aacute;rias &agrave; otimiza&ccedil;&atilde;o da economia fiscal, inclusive com implanta&ccedil;&atilde;o de Comit&ecirc; Tribut&aacute;rio &ndash; grupo de trabalho a ser formado no &acirc;mbito da empresa, com reuni&otilde;es peri&oacute;dicas e participa&ccedil;&atilde;o de profissional do escrit&oacute;rio.  <br /><br />
A assessoria tribut&aacute;ria tamb&eacute;m envolve a orienta&ccedil;&atilde;o geral da empresa sobre procedimentos na &aacute;rea tribut&aacute;ria e fiscal, inclusive sobre as altera&ccedil;&otilde;es na legisla&ccedil;&atilde;o, emiss&atilde;o de pareceres e opini&otilde;es legais. <br /><br />
Em rela&ccedil;&atilde;o &agrave; advocacia tribut&aacute;ria contenciosa, o escrit&oacute;rio patrocina a&ccedil;&otilde;es judiciais para obten&ccedil;&atilde;o de economia tribut&aacute;ria, bem como defesas administrativas e judiciais contra exig&ecirc;ncias consubstanciadas em lan&ccedil;amentos de tributos administrados pela Receita Federal do Brasil, Fazendas Estaduais e Municipais.</p>
