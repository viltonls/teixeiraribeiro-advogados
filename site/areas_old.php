Presta assessoria consultiva em aspectos societ&aacute;rios de empresas de capital aberto e fechado, representa&ccedil;&atilde;o em assembleias gerais de acionistas e reuni&otilde;es de quotistas, bem como a elabora&ccedil;&atilde;o e altera&ccedil;&atilde;o de contratos sociais, estatutos, acordos de acionistas, atas de reuni&otilde;es e resolu&ccedil;&otilde;es de s&oacute;cios e de assembleias gerais de acionistas.<br /><br />
Planeja, estrutura e implanta os instrumentos societ&aacute;rios necess&aacute;rios &agrave; forma&ccedil;&atilde;o de sociedades brasileiras resultantes de investimentos estrangeiros, sempre sob a &oacute;tica da economia tribut&aacute;ria e orientando sobre os benef&iacute;cios fiscais poss&iacute;veis.</p>
